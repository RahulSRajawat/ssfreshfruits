  <h3>Watermelon History</h3>

  <p>The searchers say the origins of watermelon have been traced back to the deserts of southern Africa.The first recorded watermelon harvest occurred about 5,000 years ago in Egypt. Watermelons were often placed in the burial tombs of kings to nourish them in the afterlife.</p>

  <p>From there; watermelons were brought to countries along the Mediterranean Sea by way of merchant ships. By the 10th century, watermelon found its way toChin, India Iran which is today one by largest watermelon producer in the worldwide. (Iranian watermelon is one of the best verities watermelons in the worldwide.</p>

  <p>Iranian Sweetness watermelon makes popular in Arabic countries, Russia, Turkey, and Iraq and also most of country are most important client for Iranian watermelon).</p>

  <h3>Where is Watermelon Growing?</h3>

  <p>Iran country is one of the best verities watermelons in the worldwide because watermelons are tropical or subtropical plants and need temperatures higher than about 25 °C (77 °F) to thrive. Therefore watermelon needs to grow in warm places. Our county weather is suitable for watermelon growing.</p>	

  <h3>How is Watermelon growing?</h3>
  <p>Watermelon seeds are usually sowed directly in the garden. It’s possible to start growing seedlings indoors and they transplant them into the garden. However, watermelons don’t like it when their root system is disturbed. Watermelons need to be planted where they get at least 6-8 hours of direct sunlight per day. Sunlight is crucial for fruit development. Most watermelon plants will produce 4-5 fruit per vine. The fruit is usually ready for harvest 75-100 days after planting, depending on the variety.</p>

  <p>Watermelons have a good resistance to disease and pest problems. If planted in nutrient rich soil, watermelons don’t usually need to be fertilized. They require a fair amount of water for the first few weeks of growth. When the fruit sets, you can almost stop watering the plants. As the fruit develops, the less water the plant gets – the better. Less water will increase the sugar content and concentration in the fruit, leading to sweeter, crisper melons.</p>

  <h3>Watermelon Harvesting Time</h3>
  <p>Watermelon harvesting time is usually harvested in spring and summer season. The watermelon you planted will be ready about 80 or so days after you plant it from seed. This means, around day 75 or so, depending on how the season was.</p>

  <h3>How to Pick a Ripe Watermelon</h3>
  <p>First, the curly green tendrils will start to yellow and turn brown. This is a sign that the plant is no longer feeding the watermelons and that the right time to pick a watermelon is at hand.<br>
Second, if you pick up a watermelon and thump it with the palm of your hand, sometimes when they are ripe, you will find that they make a hollow sound. Keep in mind that not all ripe watermelon will make this sound, so if it doesn’t make a hollow sound it doesn’t mean the melon isn’t ripe. However, if it does make the sound, it is most assuredly ready to harvest.<br>
Finally, the surface color of the watermelon will become dull. The underside of the watermelon that was on the ground will also turn light green or yellow if it is time when to pick a watermelon.</p>

